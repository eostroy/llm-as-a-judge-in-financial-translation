def paragraph(text: str, style: str = "p") -> str:
    text = escape(text)
    if style == "title":
        return (
            '<w:p><w:pPr><w:jc w:val="center"/><w:spacing w:after="240"/></w:pPr>'
            '<w:r><w:rPr><w:b/><w:sz w:val="32"/><w:szCs w:val="32"/></w:rPr>'
            f"<w:t>{text}</w:t></w:r></w:p>"
        )
    if style == "h1":
        return (
            '<w:p><w:pPr><w:spacing w:before="240" w:after="120"/></w:pPr>'
            '<w:r><w:rPr><w:b/><w:sz w:val="26"/><w:szCs w:val="26"/></w:rPr>'
            f"<w:t>{text}</w:t></w:r></w:p>"
        )
    return (
        '<w:p><w:pPr><w:firstLineChars w:val="200"/><w:spacing w:after="120"/>'
        '<w:jc w:val="both"/></w:pPr><w:r><w:rPr><w:sz w:val="22"/>'
        f'<w:szCs w:val="22"/></w:rPr><w:t>{text}</w:t></w:r></w:p>'
    )


